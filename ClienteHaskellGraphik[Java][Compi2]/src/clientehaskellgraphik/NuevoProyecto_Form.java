    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String codigo = "Proyecto "+txtnombre.getText()+"=["
                + "Permiso = "+"\""+txtpermiso.getSelectedItem().toString()+"\";";
        if(!txtcompartido.getText().equals("")){
            String[] s = txtcompartido.getText().split(",");
            codigo += "Lista = \""+s[0]+"\"";
            for(int i = 1; i<s.length; i++){
                codigo += ",\""+s[i]+"\"";
            }
            codigo += ";";
        }
        codigo += "]";
        Conexion.Conectar(codigo);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

